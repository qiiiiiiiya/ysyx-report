#define R(i) gpr(i)
#define Mr vaddr_read
#define Mw vaddr_write

enum {
  TYPE_I, TYPE_U, TYPE_S,TYPE_J,TYPE_R,TYPE_B,
  TYPE_N, // none
};

#define src1R() do { *src1 = R(rs1); } while (0)
#define src2R() do { *src2 = R(rs2); } while (0)
//I型，提取指令的为31到20位的立即数（immI），并将其符号扩展到32位
#define immI() do { *imm = SEXT(BITS(i, 31, 20), 12); } while(0)
//U型，提取指令的为31到12位的立即数（immU），低12位补零
#define immU() do { *imm = SEXT(BITS(i, 31, 12), 20) << 12; } while(0)
//S型，提取指令的为31到25位和11到7位的立即数（immS），并将其拼接成12位立即数
#define immS() do { *imm = (SEXT(BITS(i, 31, 25), 7) << 5) | BITS(i, 11, 7); } while(0)

//J型，
#define immJ() do { *imm = (SEXT(BITS(i, 31, 31), 1) << 20) | (BITS(i, 30, 21) << 1) | (BITS(i, 20, 20) << 11) | (BITS(i, 19, 12)<<12) ; } while(0)
//#define immJ() do { *imm = (SEXT(BITS(i, 31, 31), 1) << 20) | BITS(i, 30, 21) << 1 |BITS(i, 20, 20) << 11 | BITS(i, 19, 12) << 12 ; } while(0)

//R型，
#define immR() do { *imm = 0; } while(0)

//B型，
#define immB() do { *imm = (SEXT(BITS(i, 31, 31), 1) << 12) | (BITS(i, 7, 7) << 11) | (BITS(i, 30, 25) << 5) | (BITS(i, 11, 8) << 1);} while(0)
//#define immB() do { *imm = (SEXT(BITS(i, 31, 31), 1) << 11) | BITS(i,7,7)<<8 | BITS(i, 30, 25)<<4 | BITS(i,11,8)<<1; } while(0)
//#define immB() do { *imm = SEXT(BITS(i, 31, 31), 1) << 11 | ((SEXT(BITS(i, 7, 7), 1) << 63) >> 63) << 10 | ((SEXT(BITS(i, 30, 25), 6) << 58) >> 58) << 4 | ((SEXT(BITS(i, 11, 8), 4) << 60) >> 60); *imm = *imm << 1; } while (0)
static void decode_operand(Decode *s, int *rd, word_t *src1, word_t *src2, word_t *imm, int type) {
  uint32_t i = s->isa.inst;
  int rs1 = BITS(i, 19, 15);
  int rs2 = BITS(i, 24, 20);
  *rd     = BITS(i, 11, 7);
  switch (type) {
    case TYPE_I: src1R();          immI(); break;
    case TYPE_U:                   immU(); break;
    case TYPE_S: src1R(); src2R(); immS(); break;
    case TYPE_J:                 ; immJ(); break;
    case TYPE_R: src1R(); src2R(); immR(); break;
    case TYPE_B: src1R(); src2R(); immB(); break;
    case TYPE_N: break;
    default: panic("unsupported type = %d", type);
  }
}

